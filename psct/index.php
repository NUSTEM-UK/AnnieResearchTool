      <p>Welcome! These pages will guide you through our science-capital derived evaluation tools.</p>
      <p>These interactive tools were developed during the evaluation of NUSTEM, a sustained STEM project operating in the North East of England. We are sharing as we hope these tools will be of use to others evaluating STEM interventions in primary schools.</p>
      <p>We have found science capital a really useful concept for helping to shape the conversation about why (or why not) children and young people aspire to careers in STEM.  Originally science capital was described, and quantified, using data from children aged 11 years and up. However, here at NUSTEM we’re working with younger children, so we have looked at what methods and tools we could use to evaluate some of the aspects of science capital among 7-11 year olds.</p>
      <p>These pages will guide you through our two interactive evaluation tools and their associated data visualisation functions.</p>
      <ol>
        <li>STEM Knowledge and Aspirations Tool (SKAT): measures childrens’ knowledge of and aspiration for STEM jobs with reference to other jobs.</li>
        <li>Most Like Me/Most Like a Scientist Tool (MLM/S): measures children’s self-concept alongside their concept of scientists.</li>
